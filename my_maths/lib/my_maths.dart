library my_maths;

/// A Calculator.
class Calculator {
  /// Returns [value] plus 1.
  static int addOne(int value) => value + 1;
  static int add(int a, int b) => a + b;
  static int subtract(int a, int b) => a - b;
  static int multiply(int a, int b) => a * b;
  static double divide(int a, int b) => a / b;
}
